package samouczekprogramisty;

public class Zadanie3Human {
    private int age;
    private String name;

    public Zadanie3Human(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
